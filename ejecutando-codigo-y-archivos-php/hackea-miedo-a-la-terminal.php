Lista de Comandos:
1. ls : para listar nuestras opciones.
2. mkdir: para crear nueva carpeta.
3. touch: para crear nuevo archivo.
4. cat: para ver el contenido de un archivo.
5. nano: abre un editor de texto dentro de terminal y permite
escribir dentro del archivo.
(control+o : para sobreescribir archivo)
(control+x : para salir de nano)

